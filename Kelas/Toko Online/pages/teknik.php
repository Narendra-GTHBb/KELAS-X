   <h1>
    Filsafat Teknik
   </h1>
   <p>
    Apa pertimbangan terpenting saat membuat velg? Spesifikasi apa yang dibutuhkan velg berperforma tinggi? Secara umum, aspek performa yang paling mudah dikenali oleh pengguna adalah bobot yang ringan. Anda akan mengetahui perbedaan bobotnya hanya dengan mengangkat velg. Sebagian besar pengemudi juga dapat merasakan perbedaan dalam respons kemudi. Karena velg mobil adalah objek yang menggelinding, bobot velg yang ringan berperan dalam bobot inersia. Namun, fokus Enkei tidak terbatas pada bobot yang ringan. Untuk mencapai performa velg yang maksimal,
    <a href="#">
     Velg Mobil Enkei
    </a>
    juga berfokus pada "kekakuan" dan "kekuatan".
    <br>
    
    Velg berkualitas tinggi membutuhkan ketiga elemen: bobot yang ringan, kekakuan, dan kekuatan.
    <br>
    <br>
    KAKU: Kekakuan adalah dasar dari performa
    <a href="#">
     velg mobil
    </a>
Apa yang dimaksud dengan "kekakuan"? Jika velg kaku, velg akan menahan deformasi saat dikenakan gaya. Meskipun kita menganggap logam sebagai "keras", logam memang memiliki kecenderungan untuk berubah bentuk saat gaya diberikan dan kembali ke bentuk semula saat gaya dihilangkan. Kekakuan sering kali disamakan dengan kekuatan, tetapi keduanya berbeda karena kekuatan merupakan ukuran kecenderungan terhadap daya pecah. Untuk memaksimalkan kinerja ban berperforma tinggi saat ini, velg harus memiliki kekakuan tinggi untuk menjaga koneksi yang tepat antara sasis dan ban. Di sisi lain, velg dengan kekakuan rendah dapat dengan mudah berubah bentuk akibat beban dari ban. Enkei menyadari bahwa kekakuan adalah elemen terpenting untuk velg berperforma tinggi.
<br>
<br>
PENJELASAN ALUMINIUM: METODE PRODUKSI TIDAK AKAN MEMPENGARUHI KEKAKUAN
<br>
Bagaimana kita dapat meningkatkan kekakuan velg? Selama velg terbuat dari paduan aluminium, penggunaan metode produksi yang berbeda tidak akan memengaruhi kekakuan velg enkei. Itu karena semua paduan aluminium yang digunakan untuk penempaan dan pengecoran menghasilkan sifat material yang sama dengan kekakuan yang identik.
<br>
<br>
Perbandingan Paduan Aluminium Tempa dan Paduan Aluminium Cor
<br>
<br>
<img src="./Comparison_480x480.jpg" alt="">
<br>
<br>
* Modul Young adalah ukuran elastisitas, 
yang sama dengan rasio tegangan yang bekerja p
ada suatu zat terhadap regangan yang dihasilkan.
<br>
<br>
Anda mungkin berpikir proses penempaan menghasilkan material yang lebih kuat.
 Namun, ketika paduan aluminium tempa dan cor diukur dengan modulus Young, 
 hampir tidak terlihat perbedaan antara kedua metode tersebut. 
 Paduan aluminium tempa dan cor juga memiliki berat jenis yang hampir sama. 
 Oleh karena itu, jika Anda membuat bentuk velg yang sama persis menggunakan metode tempa dan cor, 
 berat velg tersebut harus sama. Lalu, apa keuntungan dari proses penempaan? 
 Yaitu kemampuan untuk menghasilkan kekuatan material yang lebih tinggi.
<br>
<br>
Secara umum, dibandingkan dengan cor, material tempa lebih unggul dalam hal kekuatan luluh dan kekuatan tarik. 
Namun, tidak peduli seberapa besar kekuatan materialnya, 
hal itu tidak dapat membenarkan pengurangan berat yang ekstrem.
<br>
<br>
Bahkan dengan kekuatan material yang memadai, jika velg tidak memiliki kekakuan, 
velg tersebut tidak dapat bekerja dengan baik bahkan dengan ban berperforma tinggi.
<br>
<br>
<img src="./mat-process_480x480.jpg" alt="">
<br>
<br>
Proses Durville Die Casting & MAT
<br>
Enkei telah mengadopsi dua teknologi – proses Durville die casting dan MAT yang memastikan velg aluminium cor
 memiliki kekuatan yang sebanding dengan velg tempa. Dalam proses Durville, 
 paduan aluminium cair dituangkan ke dalam cetakan dari sisi pelek bagian dalam,
  dengan sisi cakram menghadap ke bawah. Paduan tersebut dengan cepat didinginkan dan dipadatkan, 
  dimulai dari sisi cakram, sehingga menciptakan struktur logam yang lebih halus,
   yang secara signifikan meningkatkan kekuatan tarik, kekuatan luluh, dan perpanjangan.
<br>
<br>
Dalam proses MAT, pelek velg yang ditempatkan pada mandrel diputar dan digulung. 
Proses pemintalan ini menciptakan "aliran logam" dalam struktur logam paduan aluminium, 
yang mirip dengan sifat logam yang terlihat dalam proses penempaan.
<br>
<br>
<img src="./mat-logo_480x480.jpg" alt="">
<br>
<br>
Proses hibrida pengecoran dan penempaan ini menghasilkan
 kekuatan material yang tinggi pada velg sekaligus menjaga biaya produksi lebih terjangkau daripada velg tempa.
  Selain itu, proses ini memberikan kebebasan desain yang lebih besar pada permukaan velg.
<br>
<br>
Kemampuan Desain di balik velg terbaik
<br>
<br>    
Bagaimana Enkei memastikan velgnya memiliki kekakuan yang tinggi? Jawabannya adalah "Kemampuan Desain" Enkei.
<br>
<br>
Untuk mempertahankan kekakuan tinggi di bawah gaya yang besar,
 luas penampang velg dalam arah yang berlawanan dengan gaya yang diberikan perlu diperbesar.
  Sayangnya, semakin besar luas ini, velg akan semakin berat. Namun,
   jika tata letak, bentuk penampang jari-jari, dan profil velg dirancang dengan benar,
    velg dapat mempertahankan kekakuan tinggi tanpa
menambah berat.
<br>
<br>
Tata letak ini dimungkinkan oleh akumulasi data desain velg yang sangat 
banyak dari Enkei dan simulasi rekayasa 3D yang dibantu komputer. 
Selain itu, Enkei berupaya untuk mengurangi berat semaksimal mungkin dengan mengidentifikasi 
bagian velg tempat konsentrasi tegangan paling kecil kemungkinannya terjadi dan mengurangi area yang tidak memengaruhi kekakuan.
<br>
<br>
Teknik pemrosesan Enkei memastikan "kekuatan" velg-velgnya, 
dan kemampuan desainnya yang luar biasa memastikan "kekakuan" dan "ringan."
 Kemampuan desain Enkei adalah aset yang paling berharga dan penting untuk 
 mengembangkan velg-velg dengan performa tertinggi.
   </p>
